foreach ( $kb_pages as $slug => $def ) {
	$page = get_page_by_path( $slug );
	if ( $page && trim( $page->post_content ) !== '' ) {
		WP_CLI::log( "skip（本文あり）: /{$slug}/" );
		continue;
	}
	if ( $page ) {
		wp_update_post( array( 'ID' => $page->ID, 'post_content' => trim( $def['content'] ) ) );
		WP_CLI::log( "content set: /{$slug}/" );
	} else {
		wp_insert_post( array(
			'post_type'    => 'page',
			'post_status'  => 'publish',
			'post_title'   => $def['title'],
			'post_name'    => $slug,
			'post_content' => trim( $def['content'] ),
		) );
		WP_CLI::log( "page created: /{$slug}/" );
	}
}

